        <div class="content">
            <h3>One Night One Juz</h3>
            <p>PRIM Kansai juga mengadakan Tadarus Al-Kahfi setiap malam jumat untuk mendapatkan keutamaannya. 
                Ngaji Subuh dan Tadarus Al-Kahfi ini pada bulan Ramadhan ditiadakan sementara dan diganti dengan 
                Tadarus Ramadhan One Night One Juz yang didahului dengan Pengajian Tarhib Ramadhan oleh Ketua Majelis 
                Tabligh PP Muhammadiyah Ustadz Fathurrahman Kamal, Lc. M.Si.
            </p>
            <p>Selain Ngaji Qur’an, PRIM Kansai juga mengadakan kajian berkala lainnya seperti pengajian dalam 
                rangka Peringatan Hari Besar Islam (PHBI) seperti Isra’ Mi’raj yang diisi oleh Ustadz Arifin 
                Nugroho, Lc Host acara Poros Surga Trans 7 dan Damai Indonesiaku TVOne dan Nuzulul Qur’an yang 
                diisi oleh Wakil Ketua Majelis Tabligh PP Muhammadiyah Ustadz Fahmi Salim Zubair, Lc. MA. 
                Pengajian ini bersifat umum dan berkolaborasi dengan organisasi lainnya seperti Keluarga Masyarakat 
                Islam Indonesia (KMII) Kansai, Ikatan Persaudaraan Trainee Indonesia di Jepang (IPTIJ) Kansai, 
                Muslim Osaka, dan mengundang juga Konsul Jenderal RI di Osaka.</p>
        </div>
        <div class="content">
            <h3>Pengajian ‘Aisyiyah Wal ‘Ashri</h3>
            <p>Dinamakan Wal ‘Ashri dengan maksud mengambil spirit dari Pengajian ‘Aisyiyah Wal ‘Ashri yang 
                pernah dilaksanakan oleh KHA Dahlan dan Nyai Siti Walidah dalam rangka merintis pendirian 
                ‘Aisyiyah. Dengan harapan itu pula suatu saat akan berdiri ‘Aisyiyah di Jepang. Pengajian 
                ‘Aisyiyah Wal ‘Ashri ini sendiri sudah dilaksanakan beberapa kali dengan mengundang pembicara 
                sepert cicit KHA Dahlan dan Nyai Siti Walidah yaitu KRAyT Diah Purnamasari Isyana Jayantidiningrum 
                bersama Dyah Kalsitorini produser film Nyai Ahmad Dahlan, Ketua PP ‘Aisyiyah Dra. Siti ‘Aisyah. 
                M.Ag., serta Dr. Mufdlilah S.SiT, M.Sc. dari Universitas ‘Aisyiyah Yogyakarta dan Majelis Pembinaan 
                Kader PP ‘Aisyiyah.</p>
            <p>Selain itu juga dihadirkan narasumber Muslimah asli Jepang Hitomi Kawamura 
                Sensei atau Hitomi Aina Sensei nama muslimnya yang juga menjadi anggota PRIM Kansai sebagai pakar 
                diet yang membahas tema “Tubuh manusia terbentuk dari apa yang dimakan“. Tema-tema kajian kewanitaan 
                mulai fiqih maupun terkait kesehatan kewanitaan sangat menarik dan mendapat antusiasme dari kalangan 
                muslimah.</p>
        </div>
        <div class="content">
