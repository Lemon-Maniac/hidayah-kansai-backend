            <h2 class="judul">Sejarah berdirinya PRIM Kansai</h2>
        </div>

        <p>Di masa pandemi covid-19 ini, geliat gerakan Islam berkemajuan 
        di Jepang tak surut langkah. Maraknya pertemuan dan kajian online justru 
        menghubungkan warga-warga yang terpisah jarak antar kota. Terlintaslah ide mendirikan 
        sebuah ranting istimewa Muhammadiyah di wilayah Kansai oleh beberapa kader pelopor. 
        Sebagaimana diketahui bahwa dakwah di level akar rumput sangatlah penting bagi Muhammadiyah. 
        Sebagaimana yang disampaikan oleh Ustadz H.M. Jamaluddin Ahmad selaku Wakil Ketua Lembaga Pengembangan Cabang dan 
        Ranting PP Muhammadiyah bahwa sebagai sebuah organisasi, pusatnya Muhammadiyah memang di Pimpinan Pusat Muhammadiyah. 
        Namun sebagai suatu gerakan Islam maka “pusatnya Muhammadiyah yang sesungguhnya adalah di Cabang dan Ranting.“ 
        Disitulah pentingnya Ranting atau Cabang yang belum memiliki ranting untuk bergerak memperkuat akar dan
        membumikan Muhammadiyah sebagai gerakan Islam berkemajuan. Oleh karena itu, disinilah urgensinya mendirikan 
        ranting-ranting di wilayah Jepang.
        </p>
        <p>
        Ranting Istimewa Muhammadiyah (PRIM) Kansai didirikan pada 31 Oktober 2020 
        oleh beberapa orang kader pelopor dan keesokan harinya langsung memulai kegiatan Ngaji 
        Subuh untuk melancarkan bacaan dan hafalan Juz Amma. Ngaji Subuh ini dilakukan setiap hari 
        mengingat antusiasme warga yang ingin belajar intensif walau kadang sesekali diliburkan 
        karena alasan teknis. Selain itu PRIM Kansai juga mengadakan Tadarus Al-Kahfi setiap malam 
        jumat untuk mendapatkan keutamaannya seperti dalam hadits yang berbunyi:
        </p>
        <div class="hadist">
            <p>“Barangsiapa membaca surat Al-kahfi pada malam Jum’at, maka dipancarkan cahaya 
            untuknya sejauh antara dirinya dan Baitul ‘Atiq (Ka’bah).”</p> 
            <p>(HR An-Nasa’i dan Al Hakim).</p>
        </div>
        
        <h3>mengapa kansai ?</h3>
        <div class="bg-img">
            <img src="https://www.ana.co.jp/japan-travel-planner/kinki/img/hero.jpg" alt="">
        </div>

        <div class="mengapa">
            <h4>Pertama: kewilayahan, demografi dan posisi strategis.</h4>
            <p>Di Jepang terdapat 5 (lima) pulau utama yaitu Hokkaido, Honshu, Shikoku, 
                Kyusu dan Okinawa. Berdasarkan geografi dan budaya, Jepang terdiri dari 
                47 (empat puluh tujuh) prefektur dan 8 (delapan) wilayah. Kedelapan 
                wilayah ini adalah Hokkaido, Tohoku, Kanto, Chubu, Kansai, Chugoku, 
                Shikoku dan Kyushu. Wilayah ini bukanlah unit administratif tetapi nama 
                wilayah ini telah digunakan sejak tahun 1905 oleh pemerintah Jepang 
                dalam rangka statistik, peta, laporan cuaca, serta digunakan oleh 
                berbagai kepentingan bisnis dan institusi dalam menjalankan bisnisnya. 
                Secara demografi, wilayah Kansai memiliki penduduk sekitar 22,5 juta jiwa 
                terbesar kedua setelah wilayah Kanto. Kota Osaka sebagai kota industri dan 
                transport hub merupakan kota utama di Kansai dan terbesar kedua setelah Tokyo 
                yang berada di wilayah Kanto.</p>
        </div>

        <div class="mengapa">
            <h4>Kedua: budaya dan kearifan lokal.</h4>
            <p>Selain itu kearifan lokal juga menjadi pertimbangan. Suatu contoh kecil adalah 
                budaya berdiri di sebelah kanan ketika berada di eskalator merupakan sesuatu 
                yang khas di wilayah Kansai yang berbeda dengan wilayah lainnya di Jepang yang 
                budayanya berdiri di sebelah kiri ketika berada di eskalator. Hal ini 
                menunjukkan sesuatu yang unik yang bisa dijadikan strategi dakwah secara 
                kewilayahan maupun budaya dan kearifan lokal. Terlebih lagi ada kota Kyoto 
                sebagai kota seni dan budaya tradisional dan kota Nara sebagai ibukota Jepang 
                kuno.</p>
        </div>

        <div class="mengapa">
            <h4>Ketiga: efisiensi dan efektifitas dakwah.</h4>
            <p>Kansai terdiri dari prefektur Shiga, Kyoto, Hyogo, Osaka, Nara, dan Wakayama. 
                Warga dan kader yang terpisah dimasing-masing kota dan prefektur tersebut 
                kesulitan membentuk ranting karena keterbatasan jumlah warga. Oleh karenanya, 
                konsep ranting berdasar wilayah akan lebih mudah terwujud. Hal ini pun 
                dimungkinkan oleh Anggaran Dasar Muhammadiyah pasal 9 ayat 1 yang berbunyi 
                “Ranting adalah kesatuan anggota dalam satu tempat atau kawasan.” Wilayah 
                Kansai-pun juga merupakan suatu kawasan. Pada praktiknya, bentuk ranting di 
                Indonesia umumnya berdasar desa, namun juga ada yang berbasis komplek perumahan, 
                berbasis masjid seperti Ranting Al-Ummah atau bahkan berbasis komunitas di kawasan 
                bisnis tertentu seperti Ranting Jenderal Sudirman Jakarta yang intinya memenuhi 
                definisi tempat atau kawasan tergantung kondisi dakwah di lapangan.</p>

            <p>Dalam konteks wilayah Kansai Jepang, pendirian ranting berdasar wilayah ini bertujuan 
                untuk menghimpun warga dan kader yang tersebar di berbagai kota atau perfektur di 
                wilayah kansai dan meningkatkan rasa memiliki warga terhadap suatu ranting Muhammadiyah. 
                Bila ada kader atau warga yang tinggal terpisah sendiri di suatu prefektur tanpa naungan 
                ranting terdekat maka mereka akan segan bergabung berkegiatan. Selain itu terdapat juga 
                warga yang tinggal sendiri berjauhan di kota-kota tempat mereka bekerja atau belajar yang 
                tidak memungkinkan mereka membentuk ranting. Oleh karena itu pendirian ranting berdasar 
                wilayah di Jepang akan memudahkan terkumpulnya warga dan kader. Sebaliknya saat ini bila 
                berdasar prefektur dan kota maka sulit untuk dapat memenuhi syarat ideal sebuah ranting 
                karena masih terbatasnya jumlah kader. Berdasarkan sebaran warga Muhammadiyah di wilayah 
                ataupun prefektur, maka pendirian ranting berdasarkan wilayah di Jepang akan lebih efektif 
                dan efisien.</p>
        </div>

        <div class="mengapa">
            <h4>Keempat: Kemitraan dengan organisasi lain.</h4>
            <p>Selanjutnya pendirian ranting berdasar wilayah juga mensejajarkan suatu ranting Muhammadiyah 
                setara dengan organisasi dengan pola kewilayahan serupa lainnya seperti KMII Kansai, IPTIJ 
                Kansai maupun PPI Kansai. Kesetaraan ini memudahkan koordinasi dan kolaborasi kegiatan 
                kemitraan dan meningkatkan nama Persyarikatan Muhammadiyah di Jepang, sebagaimana dalam 
                pelaksanaan pengajian umum dan penyelenggaraan sholat Id. Inilah salah satu strategi dakwah 
                Muhammadiyah di wilayah Kansai.</p>
        </div>

